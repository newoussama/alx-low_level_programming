#include <stdio.h>
#include "3-calc.h"

/**
 * this file contain  5 functions:
 *
 * op_add: that add 2 integers
 * op_sub: that return the difference
 * op_mul: that return the mul of 2 int
 * op_div: that devide 2 integers
 * op_mod: thatgive the remainder
 *
 * Description: file to help us in our task
*/

int op_add(int a, int b)
{
	return a + b;
}

int op_sub(int a, int b)
{
	return a - b;
}

int op_mul(int a, int b)
{
	return a * b;
}

int op_div(int a, int b)
{
	return a / b;
}

int op_mod(int a, int b)
{
	return a % b;
}
